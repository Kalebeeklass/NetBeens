    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String numerosRandom = " ";
        for (int i = 0; i < 5; i++) {

            vet[i] = (int) (Math.random() * 100 + 1);
            numerosRandom += String.valueOf(vet[i]) + " ";
        }
        numeroaleatorio1.setText(numerosRandom);

        for (int i = 0; i < javaModel.getRowCount(); i++) {
            valorLinha = Integer.parseInt(javaModel.getValueAt(i, 5).toString());
            comparaçãoDiferença = Integer.MAX_VALUE;
            for (int j = 0; j < 5; j++) {
                diferença = Math.abs(valorLinha - vet[j]);
                if (diferença < comparaçãoDiferença) {
                    comparaçãoDiferença = diferença;
                    javaModel.setValueAt(diferença, i, 6);
                }
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed


