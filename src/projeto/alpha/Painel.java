    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String sexo = " ";

        if (jRadioButton1.isSelected()) {
            JOptionPane.showMessageDialog(this, "Parabéns! cadastro efetuado.\nNome:" + jTextField5.getText() + "\nIdade:" + jTextField3.getText() + "\nRG:" + jTextField1.getText() + "\nCpf:" + jTextField2.getText() + "\nGênero:Masculino");
            sexo = "masculino";
        }

        if (jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Parabéns! cadastro efetuado.\nNome:" + jTextField5.getText() + "\nIdade:" + jTextField3.getText() + "\nRG:" + jTextField1.getText() + "\nCpf:" + jTextField2.getText() + "\nGênero:Feminino");
            sexo = "feminino";
        }
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);

        if (cont >= quantidadeMaxima) {
            //JOptionPane.showMessageDialog(this, cont);
        } else {
            javaModel.addRow(new String[]{jTextField5.getText(), jTextField3.getText(), jTextField1.getText(), jTextField2.getText(), sexo, jTextField6.getText(), String.valueOf(diferença)});
            cont++;
        }

        jTable1.setModel(javaModel);
        if (limp == 0) {
            jComboBox1.removeAllItems();
            limp = 11;
        }
        jComboBox1.addItem(jTextField5.getText());
        jComboBox1.showPopup();
        jComboBox1.setSelectedIndex((jComboBox1.getItemCount() - 1));

        jTextField1.setText(" ");
        jTextField2.setText(" ");
        jTextField3.setText(" ");
        jTextField5.setText(" ");

    }//GEN-LAST:event_jButton2ActionPerformed

